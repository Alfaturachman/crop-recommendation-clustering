// Initialize GIS Map
function initGISMap() {
    if (gisMap) return; // already initialized

    gisMap = L.map('indonesia-map', {
        center: [-2.5, 118.0],
        zoom: 5,
        zoomControl: true,
        attributionControl: true,
    });

    // Use a clean, neutral map tile
    L.tileLayer(
        'https://{s}.basemaps.cartocdn.com/light_all/{z}/{x}/{y}{r}.png',
        {
            attribution:
                '&copy; <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a> contributors &copy; <a href="https://carto.com/attributions">CARTO</a>',
            subdomains: 'abcd',
            maxZoom: 19,
        },
    ).addTo(gisMap);

    // Add province markers
    indonesiaProvinceData.forEach((province) => {
        const cfg = zoneConfig[province.zone];
        const markerColor = cfg.color;

        // SVG-based colored circle marker
        const svgIcon = L.divIcon({
            className: '',
            html: `<svg xmlns="http://www.w3.org/2000/svg" width="28" height="36" viewBox="0 0 28 36">
                <path d="M14 0 C6.27 0 0 6.27 0 14 C0 24 14 36 14 36 C14 36 28 24 28 14 C28 6.27 21.73 0 14 0Z"
                      fill="${markerColor}" opacity="0.9"/>
                <circle cx="14" cy="13" r="6" fill="white" opacity="0.85"/>
            </svg>`,
            iconSize: [28, 36],
            iconAnchor: [14, 36],
            popupAnchor: [0, -38],
        });

        const marker = L.marker([province.lat, province.lng], {
            icon: svgIcon,
        });

        const popupContent = `
            <div class="map-popup">
                <div class="map-popup-title"><i class="fa-solid fa-location-dot" style="color:${markerColor};font-size:12px;"></i> ${province.name}</div>
                <div class="map-popup-zone ${cfg.cssClass}">${cfg.label}</div>
                <div class="map-popup-stats">
                    <div><i class="fa-solid fa-temperature-half popup-stat-icon"></i> Suhu<br><span>${province.temp}°C</span></div>
                    <div><i class="fa-solid fa-cloud-showers-heavy popup-stat-icon"></i> Hujan<br><span>${province.rainfall} mm</span></div>
                    <div><i class="fa-solid fa-gauge popup-stat-icon"></i> pH<br><span>${province.ph}</span></div>
                    <div><i class="fa-solid fa-droplet popup-stat-icon"></i> Lembab<br><span>${province.humidity}%</span></div>
                </div>
            </div>
        `;

        marker.bindPopup(popupContent, { maxWidth: 230 });

        marker.on('click', () => {
            selectProvince(province);
        });

        marker.addTo(gisMap);
    });
}

// Select a province and update the info panel
function selectProvince(province) {
    selectedProvince = province;
    const cfg = zoneConfig[province.zone];

    // Update info panel header
    document.getElementById('gis-province-name').textContent = province.name;

    // Update data fields
    document.getElementById('gis-zone').textContent = cfg.label;
    document.getElementById('gis-rainfall').textContent =
        `${province.rainfall} mm`;
    document.getElementById('gis-temp').textContent = `${province.temp} °C`;
    document.getElementById('gis-humidity').textContent =
        `${province.humidity} %`;
    document.getElementById('gis-ph').textContent = province.ph.toFixed(1);

    // Show data panel, hide placeholder
    document.getElementById('gis-province-content').style.display = 'none';
    document.getElementById('gis-province-data').style.display = 'flex';

    // Reset apply button state
    const btn = document.getElementById('gis-apply-btn');
    btn.classList.remove('applied');
    btn.innerHTML =
        '<i class="fa-solid fa-wand-magic-sparkles"></i> Terapkan Parameter ke Dashboard';
}

// Apply selected province GIS params to the sidebar sliders
function applyGISParams() {
    if (!selectedProvince) return;
    const p = selectedProvince;

    // Set slider values
    document.getElementById('input-N').value = p.N;
    document.getElementById('input-P').value = p.P;
    document.getElementById('input-K').value = p.K;
    document.getElementById('input-temp').value = p.temp;
    document.getElementById('input-humidity').value = p.humidity;
    document.getElementById('input-ph').value = p.ph;
    document.getElementById('input-rainfall').value = p.rainfall;

    // Update display values
    updateValue('N', p.N);
    updateValue('P', p.P);
    updateValue('K', p.K);
    updateValue('temp', p.temp);
    updateValue('humidity', p.humidity);
    updateValue('ph', p.ph);
    updateValue('rainfall', p.rainfall);

    // Update apply button to success state
    const btn = document.getElementById('gis-apply-btn');
    btn.classList.add('applied');
    btn.innerHTML =
        '<i class="fa-solid fa-check-circle"></i> Parameter Berhasil Diterapkan!';

    // Scroll to top (sidebar)
    document.querySelector('.sidebar').scrollTo({ top: 0, behavior: 'smooth' });

    // Reset after 3s
    setTimeout(() => {
        btn.classList.remove('applied');
        btn.innerHTML =
            '<i class="fa-solid fa-wand-magic-sparkles"></i> Terapkan Parameter ke Dashboard';
    }, 3000);
}

