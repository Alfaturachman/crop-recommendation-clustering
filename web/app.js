function switchModel(key) {
    activeModelKey = key;
    
    // Update active model name in UI
    const modelNameEl = document.getElementById('display-model-name');
    if (modelNameEl) {
        modelNameEl.textContent = modelNamesMap[key] || key;
    }
    
    renderEvalTable(key);
    runInference();
}

function renderEvalTable(activeKey) {
    const tbody = document.getElementById('eval-tbody');
    if (!tbody || !allModelsData) return;
    const rows = allModelsData.evaluation.map(m => {
        const isActive = m.model === activeKey;
        return `<tr class="${isActive ? 'eval-active-row' : ''}">
            <td>${isActive ? '<i class="fa-solid fa-circle-dot" style="color:var(--accent-green);font-size:10px;margin-right:6px;"></i>' : ''}${m.label}</td>
            <td class="${isActive ? 'eval-highlight' : ''}">${m.silhouette.toFixed(3)}</td>
            <td class="${isActive ? 'eval-highlight' : ''}">${m.dbi.toFixed(3)}</td>
            <td class="${isActive ? 'eval-highlight' : ''}">${m.ari.toFixed(3)}</td>
        </tr>`;
    }).join('');
    tbody.innerHTML = rows;
}

window.addEventListener('DOMContentLoaded', () => {
    // Multi-model data is embedded directly to support file:// protocol (CORS-safe)
    renderEvalTable(activeModelKey);
    runInference();
    
    // Delay GIS map init slightly to ensure container has dimensions
    setTimeout(initGISMap, 300);
});

